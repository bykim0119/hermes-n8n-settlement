# -*- coding: utf-8 -*-
"""
큐닷 n8n 데모용 가짜 데이터 생성기.
CJ 내부 공모전 데이터의 '칸 구조'만 본떠 합성 — 실제 데이터는 한 글자도 안 씀.
거래처·상품명은 추상 placeholder. 안 맞는 건마다 원인(이야기 A·B·C)을 일부러 심어둠.
"""
import csv

# 상품 카탈로그: 상품명 -> (단가, 상품유형, 상품타입)
CAT = {
    "상품A": (19900, "일반상품", "일반"),
    "상품B": (32000, "일반상품", "일반"),
    "상품C": (89000, "일반상품", "일반"),
    "상품D": (12900, "일반상품", "일반"),
    "상품E": (15500, "일반상품", "일반"),
    "상품F": (0,     "사은품",   "사은품"),  # 매출 0 (사은품/덤)
}

COLS = ["자사주문번호","거래처","주문일","사은품여부","출고예정일","주문/배송상태",
        "클레임 상태","운송장등록일자","상품순번","상품명","상품수량","덤상품수량",
        "상품유형","상품타입","매출액","자재명","초기자재수량","주문수량",
        "취소수량","반품수량","배송유형","마감구분"]

# 각 행: (거래처, 주문일, 상품명, 주문수량, 취소수량, 반품수량, 덤수량, 사은품여부, 클레임, 배송유형)
PLAN = [
    # ── 거래처A  [이야기 A: 취소6 + 반품4 = 10 차이] ─ 정산이 맞고 우리가 취소/반품 미반영
    ("거래처A", "05-04", "상품A", 40, 2, 0, 0, "N", "취소완료", "택배"),
    ("거래처A", "05-06", "상품B", 30, 0, 4, 0, "N", "반품완료", "택배"),
    ("거래처A", "05-09", "상품D", 30, 4, 0, 0, "N", "취소완료", "새벽배송"),
    ("거래처A", "05-12", "상품E", 20, 0, 0, 0, "N", "",         "택배"),
    # ── 거래처B  [이야기 B: 덤/사은품 12개 — 수량엔 잡히나 매출 0] ─ 정산이 덤까지 수량에 포함
    ("거래처B", "05-05", "상품C", 30, 0, 0, 0, "N", "",         "택배"),
    ("거래처B", "05-07", "상품B", 30, 0, 0, 0, "N", "",         "택배"),
    ("거래처B", "05-10", "상품A", 20, 0, 0, 0, "N", "",         "새벽배송"),
    ("거래처B", "05-10", "상품F",  0, 0, 0,12, "Y", "",         "택배"),  # 사은품/덤
    # ── 거래처C  [이야기 C: 정산이 6개 적은데 취소2만 설명됨 → 4개 원인불명!] ─ 진짜 문제
    ("거래처C", "05-03", "상품C", 20, 0, 0, 0, "N", "",         "택배"),
    ("거래처C", "05-08", "상품E", 20, 2, 0, 0, "N", "취소완료", "택배"),
    ("거래처C", "05-11", "상품D", 20, 0, 0, 0, "N", "",         "새벽배송"),
    # ── 거래처D  [정상: 완전 일치] ─ 오탐 안 내는지 확인용
    ("거래처D", "05-04", "상품A", 25, 0, 0, 0, "N", "",         "택배"),
    ("거래처D", "05-09", "상품B", 12, 0, 0, 0, "N", "",         "택배"),
    ("거래처D", "05-13", "상품E",  8, 0, 0, 0, "N", "",         "새벽배송"),
    # ── 거래처E  [정상: 완전 일치]
    ("거래처E", "05-05", "상품C", 10, 0, 0, 0, "N", "",         "택배"),
    ("거래처E", "05-08", "상품D", 12, 0, 0, 0, "N", "",         "택배"),
    ("거래처E", "05-12", "상품A",  8, 0, 0, 0, "N", "",         "새벽배송"),
]

def next_day(mmdd, n=1):
    m, d = mmdd.split("-")
    d2 = int(d) + n
    return f"2026-{m}-{d2:02d}"

rows = []
order_seq = 70000
for cust, day, name, qty, cancel, ret, bonus, gift, claim, ship in PLAN:
    order_seq += 1
    price, ptype, ptype2 = CAT[name]
    net = qty - cancel - ret              # 실제 출고 수량
    sales = net * price                   # 매출액 = 실출고 * 단가 (사은품은 단가 0)
    status = "배송완료" if claim == "" else ("취소완료" if "취소" in claim else "반품완료")
    waybill = "" if claim.startswith("취소") else next_day(day, 1)
    rows.append({
        "자사주문번호": f"Q2026{order_seq}",
        "거래처": cust,
        "주문일": f"2026-{day}",
        "사은품여부": gift,
        "출고예정일": next_day(day, 1),
        "주문/배송상태": status,
        "클레임 상태": claim,
        "운송장등록일자": waybill,
        "상품순번": 1,
        "상품명": name,
        "상품수량": qty,
        "덤상품수량": bonus,
        "상품유형": ptype,
        "상품타입": ptype2,
        "매출액": sales,
        "자재명": f"{name}_본품자재",
        "초기자재수량": qty,
        "주문수량": qty,
        "취소수량": cancel,
        "반품수량": ret,
        "배송유형": ship,
        "마감구분": "2026-05 월마감",
    })

with open("주문상세.csv", "w", encoding="utf-8-sig", newline="") as f:
    w = csv.DictWriter(f, fieldnames=COLS)
    w.writeheader()
    w.writerows(rows)

# ── 정산마감: 거래처가 "이만큼 정산했다"고 보낸 표 (안 맞는 건을 여기에 심음) ──
#   거래처A: 우리 net 출고수량으로 정산 → 우리 '주문수량합'과 10 차이(A)
#   거래처B: 덤상품 12개까지 수량에 포함해 정산 → 우리 주문수량합보다 12 많음(B)
#   거래처C: 이유 없이 4개 덜 정산 (취소2만 설명됨) → 원인불명(C)
#   거래처D/E: 주문수량합 그대로 → 일치
SETTLE = {
    "거래처A": 110,   # 우리 주문수량합 120, 취소6+반품4=10 → net 110 (정상)
    "거래처B":  92,   # 우리 주문수량합 80 + 덤 12 = 92 (정상)
    "거래처C":  54,   # 우리 주문수량합 60, 취소2 → 58이어야 정상인데 54 (4개 미스터리)
    "거래처D":  45,
    "거래처E":  30,
}
with open("정산마감.csv", "w", encoding="utf-8-sig", newline="") as f:
    w = csv.writer(f)
    w.writerow(["거래처","마감구분","정산수량"])
    for cust, qty in SETTLE.items():
        w.writerow([cust, "2026-05 월마감", qty])

print("생성 완료: 주문상세.csv, 정산마감.csv")
print(f"주문상세 {len(rows)}행 / 정산마감 {len(SETTLE)}개 거래처")
